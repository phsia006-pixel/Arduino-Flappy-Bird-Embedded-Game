void DrawBird(unsigned char x, unsigned char y) {
    ST7735_SetAddressWindow(x,
                            y,
                            x + BIRD_W - 1,
                            y + BIRD_H - 1);
    ST7735_RAMWR_Start();

    unsigned int idx = 0;

    for (unsigned char row = 0; row < BIRD_H; row++) {
        for (unsigned char col = 0; col < BIRD_W; col++) {

            unsigned char r = birdSprite[idx++];
            unsigned char g = birdSprite[idx++];
            unsigned char b = birdSprite[idx++];


            if (r == 0 && g == 0 && b == 0) {
                TFTColorMode(SKY_R, SKY_G, SKY_B);
            } else {
                TFTColorMode(r, g, b);
            }
        }
    }
}

void DrawRect(unsigned char x0, unsigned char y0,
                    unsigned char x1, unsigned char y1,
                    unsigned char r, unsigned char g, unsigned char b) {
    ST7735_SetAddressWindow(x0, y0, x1, y1);
    ST7735_RAMWR_Start();

    unsigned int w = (unsigned int)(x1 - x0 + 1);
    unsigned int h = (unsigned int)(y1 - y0 + 1);
    unsigned long count = (unsigned long)w * (unsigned long)h;

    for (unsigned long i = 0; i < count; i++) {
        TFTColorMode(r, g, b);
    }
}

void DrawPipe(unsigned char pipeX, unsigned char pipeGapY) {
    unsigned char x0 = pipeX;
    unsigned char x1 = pipeX + PIPE_WIDTH - 1;
    if (x1 >= SCREEN_W) {
        x1 = SCREEN_W - 1;
    }

    if (pipeGapY > 0) {
        DrawRect(x0, 0,
                       x1, (unsigned char)(pipeGapY - 1),
                       PIPE_R, PIPE_G, PIPE_B);
    }

    unsigned char bottomY0 = pipeGapY + GAP_HEIGHT;
    if (bottomY0 < SCREEN_H) {
        DrawRect(x0, bottomY0,
                       x1, (unsigned char)(SCREEN_H - 1),
                       PIPE_R, PIPE_G, PIPE_B);
    }
}

